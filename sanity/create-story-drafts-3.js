async function main() {
  const base = Date.parse('2026-07-18T17:00:00.000Z')
  let created = 0
  for (let i = 0; i < posts.length; i++) {
    const p = posts[i]
    const body = p.paras.map((t) => block(t))
    body.push(block(p.origin, 'normal', ['em']))
    const doc = {
      _id: `drafts.story-${p.slug}`,
      _type: 'post',
      title: p.title,
      slug: {_type: 'slug', current: p.slug},
      date: new Date(base + i * 1000).toISOString(),
      category: p.category,
      subcategory: p.subcategory || undefined,
      readingTime: p.reading,
      excerpt: p.excerpt,
      pullQuote: p.pull,
      featured: false,
      body,
    }
    await client.createIfNotExists(doc)
    created++
    console.log(`✓ ${p.title}`)
  }
  console.log(`\nتم: ${created} مسودة في الدفعة الثالثة (drafts.* — لا يُنشر منها شيء).`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
